export const modal = {
  popout: document.querySelector("#modal-wrapper"),
  popOut: document.querySelector("#popout"),

  open() {
    modal.popOut.style.display = "flex";
    modal.popout.style.display = "flex";
  },

  close() {
    modal.popOut.style.display = "none";
    modal.popout.style.display = "none";
  },
};
